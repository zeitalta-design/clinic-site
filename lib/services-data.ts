export interface ServiceItem {
  /** URLスラッグ（例: "diabetes"） */
  slug: string;
  /** ページタイトル */
  title: string;
  /** H1見出し（地域名入り） */
  h1: string;
  /** SEO用 title（ブラウザタブ・検索結果に表示） */
  seoTitle: string;
  /** 一覧カード用の短い説明（1行） */
  cardDescription: string;
  /** ページ上部の導入文 */
  introduction: string;
  /** SVGアイコンの path（d属性） */
  iconPath: string;
  /** クリニックでご相談いただけること */
  consultations: string[];
  /** 補足メッセージ（ページ下部） */
  closingMessage: string;
  /** SEO description */
  metaDescription: string;
  /** 関連する診療項目のスラッグ（内部リンク強化用） */
  relatedSlugs: string[];
  /** 関連リンクの導入文（内部リンクSEO強化用） */
  relatedText: string;
  /** ページ上部の症状訴求ブロック（任意） */
  symptomAlert?: {
    heading: string;
    symptoms: string[];
    message: string;
  };
  /** 放置リスク説明ブロック（任意） */
  riskBlock?: {
    heading: string;
    paragraphs: string[];
  };
  /** 受診の目安ブロック（任意） */
  consultGuide?: {
    heading: string;
    items: string[];
    message: string;
  };
  /** 追加の内部リンク導線（任意） */
  additionalLinks?: { label: string; href: string; description: string }[];
  /** 補足ブロック（任意、例: 女性に多い症状） */
  supplementBlock?: {
    heading: string;
    paragraphs: string[];
  };
  /** 追加コンテンツセクション（任意） */
  extraSections?: { heading: string; content: string }[];
}

